
public class Q21 {
	public static void main(String[] args) {
		/*
		 * - do-while loop executes the body first.
		 * - Then the boolean expression is checked.
		 * - Since the boolean expression is the value false,
		 * the loop will be terminated. 
		 */
		do {
			System.out.println("helium");
		}while (false);
	}
}
