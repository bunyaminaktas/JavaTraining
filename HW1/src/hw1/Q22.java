public class Q22 { // An interface or class keyword is expected after public keyword.
	double count; // double and int, both are variable types. They cannot define a variable at the same time
	void errors() {} // void methods don't return a result so it is okay.
	static private int limit; // private cannot be written after return type. void is invalid type for variables.
}


